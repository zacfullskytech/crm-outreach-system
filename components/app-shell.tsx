export function AppShell({ children, isAdmin = false }: { children: React.ReactNode; isAdmin?: boolean }) {
  const pathname = usePathname();
  const router = useRouter();
  const [userEmail, setUserEmail] = useState<string | null>(null);

  useEffect(() => {
    let active = true;

    createClient()
      .then((supabase) => supabase.auth.getUser())
      .then(({ data }) => {
        if (active) {
          setUserEmail(data.user?.email ?? null);
        }
      })
      .catch(() => {
        if (active) {
          setUserEmail(null);
        }
      });

    return () => {
      active = false;
    };
  }, []);

  async function handleLogout() {
    const supabase = await createClient();
    await supabase.auth.signOut();
    router.push("/login");
    router.refresh();
  }

  return (
    <div className="shell">
      <aside className="sidebar">
        <div className="sidebar-brand">
          <div className="sidebar-brand-mark" aria-hidden="true">
            <span>FS</span>
          </div>
          <div>
            <h1>Full Sky Technologies</h1>
            <div className="tagline">
              Client Management <span>|</span> CRM
            </div>
          </div>
        </div>
        <div className="sidebar-section-label">Workspace</div>
        <nav className="nav">
          {navItems
            .filter((item) => !item.adminOnly || isAdmin)
            .map(({ label, href, icon }) => (
              <Link key={href} href={href as any} className={pathname === href ? "active" : ""}>
                <span className="nav-icon">{icon}</span>
                <span className="nav-label">{label}</span>
              </Link>
            ))}
        </nav>
        <div className="sidebar-footer">
          <div className="sidebar-user-card">
            <div className="sidebar-user-title">Signed in</div>
            {userEmail ? (
              <p className="user-email" title={userEmail}>{userEmail}</p>
            ) : (
              <p className="user-email">Current session</p>
            )}
          </div>
          <button className="logout-btn" onClick={handleLogout}>
            <svg viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round">
              <path d="M9 21H5a2 2 0 0 1-2-2V5a2 2 0 0 1 2-2h4" />
              <polyline points="16 17 21 12 16 7" />
              <line x1="21" y1="12" x2="9" y2="12" />
            </svg>
            Sign out
          </button>
        </div>
      </aside>
      <main className="content">{children}</main>
    </div>
  );
}
